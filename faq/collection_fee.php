<div style="background-color:#7c7c7c; color:white; padding:20px; font-size:22px; margin-left:20px;">7. COLLECTION FEES</div>

<div style="background-color:#b8b8b8; color:white; padding:20px; font-size:22px; margin-left:20px; line-height:150%;">Why is a collection fee charged on arrear payments?</div>

<p style="padding:0px 20px 0px 40px;">As per the Magistrate's Court Act, 1944 Annexure B, a collection fee is payable on all arrear amounts, with the prescribed fee of 10% (VAT exclusive) of the instalment received. This act is applied to all our accounts and is enforceable by collection agencies.</p>

